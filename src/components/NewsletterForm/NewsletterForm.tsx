'use client';

import { useState, FormEvent } from 'react';

export default function NewsletterForm() {
  const [email, setEmail] = useState('');
  const [name, setName] = useState('');
  const [city, setCity] = useState('');
  const [loading, setLoading] = useState(false);
  const [ok, setOk] = useState<boolean | null>(null);
  const [err, setErr] = useState<string | null>(null);

  async function onSubmit(e: FormEvent) {
    e.preventDefault();
    setLoading(true);
    setOk(null);
    setErr(null);

    try {
      const res = await fetch('/api/subscribe', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          email,
          name,
          city,
          // honeypot: campo hidden; lasciarlo vuoto in uso reale
          honeypot: (document.getElementById('company') as HTMLInputElement)?.value || ''
        }),
      });

      const data = await res.json().catch(() => ({}));
      if (!res.ok || !data?.ok) {
        setOk(false);
        setErr(data?.error || 'Errore');
      } else {
        setOk(true);
        setEmail('');
        setName('');
      }
    } catch {
      setOk(false);
      setErr('Network error');
    } finally {
      setLoading(false);
    }
  }

  return (
    <form onSubmit={onSubmit} className='formSubscribe'>
      <label className='labelInput'>
        Email Address*
        <input
          type="email"
          className="inputFormSubs"
          required
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          autoComplete="email"
          disabled={loading}
          style={{ width: '100%', padding: '10px'}}
        />
      </label>

      <label className='labelInput'>
        Name*
        <input
          type="text"
          className="inputFormSubs"
          value={name}
          onChange={(e) => setName(e.target.value)}
          autoComplete="name"
          disabled={loading}
          style={{ width: '100%', padding: '10px'}}
        />
      </label>

      <label className='labelInput'>
        City*
        <input
          type="text"
          className="inputFormSubs"
          value={city}
          onChange={(e) => setCity(e.target.value)}
          autoComplete="city"
          disabled={loading}
          style={{ width: '100%', padding: '10px'}}
        />
      </label>

      {/* Honeypot anti-bot (nascosto a vista/AT) */}
      <div aria-hidden="true" style={{ position: 'absolute', display:'none' }}>
        <label>
          Company
          <input id="company" name="company" type="text" tabIndex={-1} autoComplete="off" />
        </label>
      </div>

     

      <button
        type="submit"
        className='submitFormSubs'
        disabled={loading}
       
      >
        {loading ? 'Iscrizione…' : 'Subscribe \u2192'}
      </button>

      {ok === true && <p style={{ color: 'green' }}>Controlla la tua email per confermare l’iscrizione.</p>}
      {ok === false && <p style={{ color: 'crimson' }}>Errore: {err}</p>}
    </form>
  );
}
